#include "ft_printf.h"

int	ft_putstr(const char *s)
{
	int	count;

	if (!s)
		s = "(null)";
	count = 0;
	while (s[count] != '\0')
	{
		if (ft_putchar(s[count]) < 1)
			return (-1);
		count++;
	}
	return (count);
}
